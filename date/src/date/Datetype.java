public class Datetype {
    private int year;
    private int month;

    public  int getYear() {
        return this.year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public  int getMonth() {
        return this.month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public  int getDay() {
        return this.day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    private int day;
}
